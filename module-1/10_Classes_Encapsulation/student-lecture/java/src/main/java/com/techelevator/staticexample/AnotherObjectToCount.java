package com.techelevator.staticexample;

public class AnotherObjectToCount {

	   //this is an example of an object counter that does not have a static variable
	   public int count = 0;
	     
	   public void increment()
	   {
	       this.count++;
	   }	
	   
	   public int getCount() {
		   return count;
	   }
	
	
}
